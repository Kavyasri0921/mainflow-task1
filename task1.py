# Create a list
print("List:")
my_list = [1, 2, 3, 4, 5]
print(my_list)

# Add an element to the list
my_list.append(6)
print(my_list)

# Remove an element from the list
my_list.remove(3)
print(my_list)

# Modify an element in the list
my_list[0] = 10
print(my_list)

# Create a dictionary
print("Dictionary:")
my_dict = {'a': 1, 'b': 2, 'c': 3}
print(my_dict)

# Add a key-value pair to the dictionary
my_dict['d'] = 4
print(my_dict)

# Remove a key-value pair from the dictionary
del my_dict['b']
print(my_dict)

# Modify a value in the dictionary
my_dict['a'] = 100
print(my_dict)


# Create a set
print("Set:") 
